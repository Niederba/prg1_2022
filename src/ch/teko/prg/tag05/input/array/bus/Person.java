package ch.teko.prg.tag05.input.array.bus;

public class Person {
    private String name;
    private String vorName;

    public Person(String name, String vorName) {
        this.name = name;
        this.vorName = vorName;
    }

    public String getName() {
        return name;
    }

    public String getVorName() {
        return vorName;
    }
}
